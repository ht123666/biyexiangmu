$(function(){
    $(window).scroll(function(){
        //得到滚动条距离窗口顶部的距离
        var distance=$(this).scrollTop();
        //console.log(distance);
        if(distance>=300){
            $("#listBanner").addClass("list_banner_hover");
        }else{
            $("#listBanner").removeClass("list_banner_hover");
        }
    })
});